    private void tbl_detailPenggunaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_detailPenggunaMouseClicked
        int rowNo = tbl_detailPengguna.getSelectedRow();
        if (rowNo == -1) {
            return;
        }
        TableModel m = tbl_detailPengguna.getModel();

        text_idUser.setText(m.getValueAt(rowNo, 0).toString());
        text_Nama.setText(m.getValueAt(rowNo, 1).toString());
        text_Email.setText(m.getValueAt(rowNo, 2).toString());
        text_Telp.setText(m.getValueAt(rowNo, 3).toString());

        EditUser.setEnabled(true);
        HapusUser.setEnabled(true);
    }//GEN-LAST:event_tbl_detailPenggunaMouseClicked

