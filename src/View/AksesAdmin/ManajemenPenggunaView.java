    private void TambahPenggunaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahPenggunaActionPerformed
        try {
            int idBaru = Integer.parseInt(text_idUser.getText());
            String namaBaru = text_Nama.getText();
            String Email = text_Email.getText();
            String Telepon = text_Telp.getText();
            String password = "user123";
            int role = 2;

            User userBaru = new User(idBaru, namaBaru, password, Email, Telepon,role);
            userBaru.setidUser(idBaru);
            userBaru.setNamaPengguna(namaBaru);
            userBaru.setPasswordPengguna(password);
            userBaru.setEmail(Email);
            userBaru.setNomorTelepon(Telepon);
            userBaru.setIdRole(role);

            controller.addUser(userBaru);

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this,
                    "Semua field wajib diisi dan sesuai dengan format",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_TambahPenggunaActionPerformed

    private void EditUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditUserActionPerformed
        if (text_idUser.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silakan pilih baris terlebih dahulu.");
            return;
        }
        try {
            int idBaru = Integer.parseInt(text_idUser.getText());
            String namaBaru = text_Nama.getText();
            String Email = text_Email.getText();
            String Telepon = text_Telp.getText();

            User UserTerpilih = new User(idBaru, namaBaru, Email, Telepon);
            UserTerpilih.setidUser(idBaru);
            UserTerpilih.setNamaPengguna(namaBaru);
            UserTerpilih.setEmail(Email);
            UserTerpilih.setNomorTelepon(Telepon);

            controller.editUser(UserTerpilih);

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this,
                    "Format angka salah: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_EditUserActionPerformed

    private void HapusUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusUserActionPerformed
        int rowNo = tbl_detailPengguna.getSelectedRow();
        if (rowNo == -1) {
            JOptionPane.showMessageDialog(this,
                    "Silakan pilih baris yang akan dihapus.",
                    "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int idForm = Integer.parseInt(text_idUser.getText());
        String namaForm = text_Nama.getText();
        String emailForm = text_Email.getText();
        String nomorTeleponForm = text_Telp.getText();

        User UserForm = new User(idForm, namaForm, emailForm, nomorTeleponForm);
        UserForm.setidUser(idForm);
        UserForm.setNamaPengguna(namaForm);
        UserForm.setEmail(emailForm);
        UserForm.setNomorTelepon(nomorTeleponForm);

        int konfirm = JOptionPane.showConfirmDialog(this,
                "Yakin ingin menghapus User \"" + namaForm + "\"?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION);
        if (konfirm != JOptionPane.YES_OPTION) {
            return;
        }

        controller.deleteUser(UserForm);
    }//GEN-LAST:event_HapusUserActionPerformed

